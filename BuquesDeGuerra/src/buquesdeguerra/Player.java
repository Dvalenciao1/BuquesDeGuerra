package buquesdeguerra;

public class Player {

    private int BarcosGrandes = 3;
    private int BarcosPequenos = 2;
    private int puntos = 0;
    private int ronda = 0;

    public int getBarcosGrandes() {
        return BarcosGrandes;
    }

    public void setBarcosGrandes(int BarcosGrandes) {
        this.BarcosGrandes = BarcosGrandes;
    }

    public int getBarcosPequenos() {
        return BarcosPequenos;
    }

    public void setBarcosPequenos(int BarcosPequenos) {
        this.BarcosPequenos = BarcosPequenos;
    }

    public int getPuntos() {
        return puntos;
    }

    public void setPuntos(int puntos) {
        this.puntos = puntos;
    }

    public int getRonda() {
        return ronda;
    }

    public void setRonda(int ronda) {
        this.ronda = ronda;
    }
    
    
}
